import type { Topic } from "./topics";

// Спільний системний промпт: модель діє як доброзичливий репетитор з математики.
export function tutorSystemPrompt(topic: Topic): string {
  return [
    "Ти — доброзичливий і терплячий репетитор з математики для студентів коледжу (професійна освіта).",
    "Спілкуйся виключно українською мовою, простою й зрозумілою, без зверхності.",
    "Пояснюй крок за кроком, наводь приклади, заохочуй студента. Не давай готову відповідь одразу, якщо студент сам може дійти — підштовхуй навідними питаннями.",
    "Відповідай коротко й по суті, використовуй прості позначення для формул.",
    "",
    `Поточна тема: «${topic.title}».`,
    `Теорія теми: ${topic.theory}`,
    `Задача, над якою працює студент: ${topic.question}`,
  ].join("\n");
}

// Промпт для перевірки відповіді студента.
export function checkAnswerPrompt(topic: Topic, userAnswer: string): string {
  return [
    `Тема: ${topic.title}.`,
    `Задача: ${topic.question}`,
    `Правильна відповідь: ${topic.answer}`,
    `Відповідь студента: ${userAnswer}`,
    "",
    "Оціни, чи правильна відповідь студента (враховуй еквівалентні форми запису, напр. «6x+5» та «5+6x»).",
    "Дай коротке пояснення українською: якщо правильно — похвали й поясни чому; якщо ні — м'яко вкажи на помилку та підкажи напрямок, але НЕ давай повністю готовий розв'язок.",
  ].join("\n");
}
